def addValues(param01, param02) :
    # function to add two values, accepting parameters and returning value
    
    result = param01 + param02
    
    print(f"\nThe Sum of {param01} And {param02} is : ", end = "")
    return result

def subtractValues(param01, param02) :
    # function to subtract two values, accepting parameters and returning value
    
    result = param01 - param02
    
    print(f"\nThe Difference of {param01} And {param02} is : ", end = "")
    return result

